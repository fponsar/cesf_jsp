package com.calculadora2;

public class Calculadora {
	static String suma(int a, int b) {
		return "suma = " + (a+b) ;
	}
	static String resta(int a, int b) {
		return "resta = " + (a-b) ;
	}
	static String multiplica(int a, int b) {
		return "multiplicació = " + (a*b);
	}
	static String divide(int a, int b) {
		return "divisió = " + (a/b) ;
	}

}
